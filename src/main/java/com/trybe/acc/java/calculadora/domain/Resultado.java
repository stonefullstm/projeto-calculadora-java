package com.trybe.acc.java.calculadora.domain;

/**
 * Class Resultado.
 *
 */
public class Resultado {
  private int valor;

  /**
   * Construtor.
   */
  public Resultado(int valor) {
    super();
    this.valor = valor;
  }

  /**
   * getValor.
   */
  public int getValor() {
    return valor;
  }

  /**
   * setValor.
   */
  public void setValor(int valor) {
    this.valor = valor;
  }
}
